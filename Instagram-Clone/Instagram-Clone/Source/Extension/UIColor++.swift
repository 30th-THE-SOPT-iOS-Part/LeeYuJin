import UIKit

extension UIColor {
    
    @nonobjc class var black: UIColor {
        return UIColor(red: 10.0 / 255.0, green: 11.0 / 255.0, blue: 16.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var textGray: UIColor {
        return UIColor(red: 114.0, green: 114.0, blue: 114.0, alpha: 1.0)
    }
    
    @nonobjc class var textfieldStroke: UIColor {
        return UIColor(red: 243.0 / 255.0, green: 245.0 / 255.0, blue: 248.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var textfieldFill: UIColor {
        return UIColor(red: 250.0 / 255.0, green: 250.0 / 255.0, blue: 250.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var accentBlue: UIColor {
        return UIColor(red: 55.0 / 255.0, green: 151.0 / 255.0, blue: 239.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var accentLightBlue: UIColor {
        return UIColor(red: 149.0 / 255.0, green: 200.0 / 255.0, blue: 248.0 / 255.0, alpha: 1.0)
    }
    
    
    
    
    
    
}
